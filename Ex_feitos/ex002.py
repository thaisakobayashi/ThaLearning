"""Crie um script Python que leia o dia,
o mês e o ano de nascimento de uma pessoa e
mostra uma mensagem com a data formatada."""

dia = input('Que dia você nasceu? ')
mes = input('Qual o mês do seu nascimento? ')
ano = input('Em qual ano você nasceu? ')
print('Eu nasci no', dia, 'de', mes, 'de', ano, '.')

print('Eu nasci no dia', dia, 'de', mes, 'de', ano + '.')

print('Eu nasci no dia: ' + dia + '/' + mes + '/' + ano)

print('Eu nasci no dia: ', dia, '/', mes, '/', ano)

print('Você nasceu no dia' + dia + 'de' + mes + 'de' + ano + '.' + 'Correto?')

print('Você nasceu no dia' , dia , 'de' , mes , 'de' , ano + '.' , 'Correto?')

